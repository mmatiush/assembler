int			ft_swap_uint_bytes(unsigned n)
{
	unsigned res;

	res = ((n >> 24) & 0xff) |
			((n << 8) & 0xff0000) |
			((n >> 8) & 0xff00) |
			((n << 24) & 0xff000000);
	return (res);
}
